        Interferer_1_Type=is_1_type,
        Interferer_2_Lvl=is_2_Lvl,
        Interferer_2_Freq=is_2_freq,
        Interferer_2_Type=is_2_type,
        BLER_Spec_Max=bler_spec_max,
